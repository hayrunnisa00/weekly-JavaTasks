public class Task3_Solution {
    public static void oddOrEven(int input) {

        if (input % 2 != 0) {
            System.out.println("Odd");
        } else {
            System.out.println("Even");
        }
    }

        public static void main(String[] args) {
            oddOrEven(3);

        }

    }


