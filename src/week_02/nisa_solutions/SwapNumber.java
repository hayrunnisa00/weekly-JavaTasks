package week_02.nisa_solutions;

public class SwapNumber {
    public static void main(String[] args) {

        int num1 = 4;
        int num2 = 7;

        System.out.println("num1 = " + num1);
        System.out.println("num2 = " + num2);

        num1 = num1 + num2;
        num2 = num1 - num2;
        num1 = num1 - num2;


        System.out.println("num1 = " + num1);
        System.out.println("num2 = " + num2);






    }
}
